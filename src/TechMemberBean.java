public class TechMemberBean {

	private int techNum;
	private String techName;
	private String techPhone;
	private String techLv;

	
	
	public int getId() {
		return techNum;
	}
	public void setId(int techNum) {
		this.techNum = techNum;
	}
	public String getName() {
		return techName;
	}
	public void setName(String techName) {
		this.techName = techName;
	}
	public String getPhone() {
		return techPhone;
	}
	public void setPhone(String techPhone) {
		this.techPhone = techPhone;
	}
	public String getTeam() {
		return techLv;
	}
	public void setTeam(String techLv) {
		this.techLv = techLv;
	}
	
	
}
